    private void aceptarBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarBActionPerformed
        String contraseña = String.valueOf(contraseñaPF.getPassword());
        if(contraseña.equals(String.valueOf(repetirConPF.getPassword()))){
            ArrayList parametros = new ArrayList();
            parametros.add(idTF.getText());
            parametros.add(nombreTF.getText());
            parametros.add(correoElectronicoTF.getText());
            parametros.add(contraseña);
            parametros.add(telefonoTF.getText());
            parametros.add(direccionTF.getText());
            parametros.add((String)epsCB.getSelectedItem());
            parametros.add(2);
            try {
                dba.procedureWrite("CREATE_ACC_CONFIRMATION(?,?,?,?,?,?,?,?)",parametros);
                alerta.setText("Se Creo su Cuenta Exitosamente");
            } catch (SQLException ex) {
                alerta.setText("La identificacion o el Email ya existe, o algun Campo es Erroneo");
                try {    
                    dba.getConexion().rollback();
                } catch (SQLException ex1) {
                    System.out.println("Error: Cargar la Base de Datos desde la creacion de la cuenta");
                }
            }
        }
    }//GEN-LAST:event_aceptarBActionPerformed

